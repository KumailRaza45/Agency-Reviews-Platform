export const cleanObject = (obj: any) => {
  return Object.fromEntries(
    Object.entries(obj).filter(
      ([key, value]) =>
        value !== null &&
        value !== undefined &&
        !(Array.isArray(value) && value.length === 0) &&
        value !== ""
    )
  );
};

export const getAbbreviatedLocationUsingGeocoder = (results) => {
  const countryAbbreviation = results?.address_components.find((component) =>
    component.types.includes("country")
  )?.short_name;

  console.log(countryAbbreviation);

  const countryFullname = results?.address_components.find((component) =>
    component.types.includes("country")
  )?.long_name;

  const stateAbbreviation = results?.address_components.find((component) =>
    component.types.includes("administrative_area_level_1")
  )?.short_name;

  const cityAbbreviation = results?.address_components.find((component) =>
    component.types.includes("locality")
  )?.short_name;

  const cityFullname = results?.address_components.find((component) =>
    component.types.includes("locality")
  )?.long_name;

  //if location is within US
  if (countryAbbreviation && countryAbbreviation === "US") {
    let abbreviatedLocation = "";

    if (stateAbbreviation && cityAbbreviation) {
      abbreviatedLocation = `${cityAbbreviation}, ${stateAbbreviation}`;
    } else {
      abbreviatedLocation = stateAbbreviation || "";
    }

    return abbreviatedLocation;
  } else if (countryAbbreviation && countryAbbreviation !== "US") {
    //if location is outside of US
    let abbreviatedLocation = "";

    if (countryFullname && cityFullname) {
      abbreviatedLocation = `${cityFullname}, ${countryFullname}`;
    } else {
      abbreviatedLocation = countryFullname || "";
    }

    return abbreviatedLocation;
  } else {
    return results?.formatted_address;
  }
};

export const stringToSlug = (inputString: any) => {
  // Convert the string to lowercase
  let slug = inputString.toLowerCase();

  // Replace spaces and special characters with hyphens
  slug = slug.replace(/[^a-zA-Z0-9-]/g, '-');

  // Remove multiple hyphens
  slug = slug.replace(/-+/g, '-');

  // Remove leading and trailing hyphens
  slug = slug.replace(/^-+|-+$/g, '');

  return slug
};
